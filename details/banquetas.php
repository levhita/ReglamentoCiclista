<p>A menos que seas menor de 12 años, evita circular sobre la banqueta, no sólo molestas si no
que es peligroso.</p>

<p>Por supuesto no siempre podrás evitar subirte a la banqueta, en especial cuando hace falta
infraestructura para tí, pero en esos casos evita primero circular por las calles conflictivas, y
como segunda opción usa la banqueta.</p>
