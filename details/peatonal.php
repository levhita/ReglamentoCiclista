<p>Cuando estás en zona peatonal eres tan sólo un invitado, se cortés y baja tu velocidad a la del peatón,
cuídalo y definitivamente no uses la campana.</p>

<p>Es comprensible que debido a la falta de infraestructura o como ese ultimo tramo de tu viaje
tengas que circular en espacios diseñados para peatones, sólo recuerda que a ti no te gustaría
que un automovilista se buscara espacio entre un grupo de ciclistas, pues bien, los peatones sienten
lo mismo hacia tí cuando te abres paso a través de ellos.</p>
